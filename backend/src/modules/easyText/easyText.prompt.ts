import type { CoreFields, ActionStep } from '../../common/types.js';
import type { WordClassification } from './dictionary.client.js';

export function buildEasyTextPrompt(
  rawText: string,
  coreFields: CoreFields,
  wordClassifications: WordClassification[],
  documentType?: string,
  actionSteps?: ActionStep[],
): string {
  const wordSummary = wordClassifications.length
    ? wordClassifications.map(w => {
        const definition = w.meaning ? ` / 뜻: ${w.meaning}` : '';
        const example = w.example ? ` / 예문: ${w.example}` : '';
        return `  - "${w.word}" (${w.grade})${definition}${example}`;
      }).join('\n')
    : '  (없음)';

  return `### MODULE: easyText

너는 느린학습자 학생을 위한 학교 문서 쉬운글 변환기다.

너의 작업은 요약이 아니다.
너의 작업은 문서를 새롭게 재구성하는 것도 아니다.
INPUT_TEXT의 원문 순서와 의미 단위를 유지하면서, 문장과 단어를 더 쉽게 바꾸는 것이다.

CORE_FIELDS는 날짜, 시간, 장소, 연락처 같은 정보값을 바꾸지 않기 위한 보호 기준이다.
WORD_CLASSIFICATIONS는 단어 난이도와 뜻풀이 대상을 판단하기 위한 기준이다.

# 1. 최우선 규칙

반드시 지킨다.

1. 원문 순서를 유지한다.
2. 원문 의미 단위를 임의로 합치거나 새 순서로 재배열하지 않는다.
3. 원문에 있는 안내 정보는 유지한다.
4. 인사말, 감사 표현, 학교 구호, 반복 협조 문구만 생략할 수 있다.
5. 날짜, 시간, 장소, 대상, 조건, 연락처, 준비물, 신청 방법, 제출기한은 절대 삭제하지 않는다.
6. 원문에 없는 정보는 추가하지 않는다.
7. 원문에 없는 예시는 만들지 않는다.
8. 학교명, 기관명, 행사명, 프로그램명, 서비스명은 원문 그대로 쓴다.
9. 숫자, 날짜, 시간, 전화번호, 금액, 인원은 원문 의미 그대로 유지한다.
10. 한 줄에는 한 문장만 쓴다.
11. 의미 단위가 바뀌면 빈 줄을 넣는다.

# 2. 대상 독자

출력문은 느린학습자 학생이 읽는 글이다.
원문이 학부모 대상 문서여도 출력은 학생이 이해할 수 있는 문장으로 쓴다.

보호자가 해야 하는 행동은 학생 기준으로 바꾼다.
- 학부모님께서 작성하세요 → 보호자와 함께 적어요
- 자녀와 상의하세요 → 보호자와 함께 이야기해요
- 보호자 서명이 필요합니다 → 보호자에게 이름을 써 달라고 해요
- 선생님께 제출하세요 → 선생님께 내요

아래 표현은 절대 쓰지 않는다.
- 자녀와 상의하세요
- 학부모님께서는
- 작성해 주시기 바랍니다
- 응답하여 주시기 바랍니다
- 실시합니다
- 시행하지 않습니다
- 간주됩니다
- 지출합니다
- 알려드립니다

# 3. 문체 규칙

모든 Level은 "~해요", "~예요", "~이에요"체로 쓴다.
Level 3도 "~합니다", "~습니다", "~해 주시기 바랍니다"를 쓰지 않는다.

아래 표현은 절대 쓰지 않는다.
- 실시합니다 → 해요
- 시행하지 않습니다 → 하지 않아요
- 간주됩니다 → ~로 봐요
- 지출합니다 → 돈을 내요
- 제출해야 합니다 → 내요
- 응답해 주시기 바랍니다 → 표시해요

# 4. 원문 변환 방식

원문을 아래 방식으로 바꾼다.

- 긴 문장은 짧은 문장으로 나눈다.
- 어려운 단어는 쉬운 단어로 바꾼다.
- 행정 표현은 일상적인 표현으로 바꾼다.
- 수식어가 길면 줄인다.
- 조건과 결과가 함께 있으면 문장을 나눈다.
- 괄호 안 정보는 필요한 경우 쉬운 문장으로 풀어 쓴다.
- 같은 내용이 반복되면 한 번만 쓴다.
- 단, 반복을 줄이더라도 핵심 정보는 삭제하지 않는다.

# 3. 생략할 수 있는 것

아래 문구만 생략할 수 있다.

- 학부모님 안녕하십니까?
- 건강과 행복을 기원합니다.
- 깊이 감사드립니다.
- 많은 관심 부탁드립니다.
- 협조 부탁드립니다.
- 학교 구호
- 같은 의미가 반복되는 안내 문장
- 문서 제목, 학교명, 발행자, 담당자, 전화번호로 구성된 헤더 정보
  (이 정보는 핵심 요약에 이미 포함되어 있어 쉬운글에서 반복하지 않는다.)
  단, 본문 안에서 문의 연락처가 안내 목적으로 다시 나오면 유지한다.

그 외의 날짜, 장소, 대상, 조건, 연락처, 기념품, 준비물, 신청 방법, 기간 정보는 생략하지 않는다.

# 6. Level별 변환 기준

모든 Level은 같은 원문 정보를 담는다.
Level 차이는 정보 삭제가 아니라 어휘 난이도와 설명 방식 차이다.

쉬운글은 헤더나 제목으로 시작하지 않는다.
문서의 핵심 목적이나 행동을 첫 문장으로 자연스럽게 시작한다.

## Level 1: 더 쉽게

- 한 문장은 7어절 이내로 쓴다.
- 행정어를 남기지 않는다.
- 어려운 단어는 쉬운 말로 완전히 대체한다.
- 학교 핵심어가 꼭 필요하면 바로 뒤에 한 문장으로 설명한다.
- 설명은 본문 안에 바로 넣고 말풍선에 의존하지 않는다.
- 조건문은 "~이면 ~해요" 형식으로 쓴다.
- 숫자, 비율, 날짜는 의미를 그대로 유지한다.
  나쁜 예: "정해진 비율이 안 되면"
  좋은 예: "희망한 사람이 정해진 기준보다 적으면"

## Level 2: 기본

- 한 문장은 10어절 이내로 쓴다.
- 학교 핵심어를 일부 유지한다.
- 핵심어가 처음 나올 때 바로 뒤 문장에 뜻을 붙인다.
  예: "수요조사예요.\\n수요조사는 원하는지 물어보는 조사예요."
  예: "수익자 부담이에요.\\n수익자 부담은 우유를 먹는 집에서 돈을 내는 방식이에요."
- 어려운 단어는 difficultWords에 넣는다.
- 조건문은 "~이면 ~해요" 형식으로 쓴다.

## Level 3: 더 어렵게

- 한 문장은 13어절 이내로 쓴다.
- 원문 학교 문서 표현을 가장 많이 유지한다.
- 어려운 단어는 본문에서 설명하지 않고 difficultWords에 넣는다.
- 원문에 있는 배경 설명은 유지할 수 있다.
- 원문에 없는 배경 설명은 추가하지 않는다.
- 문장 구조는 원문보다 쉽게 바꾼다.

# 5. 단어 처리 기준

WORD_CLASSIFICATIONS는 한국어기초사전(KRDICT) 기준이다.
등급은 초급, 중급, 고급, 없음, school_core 중 하나다.

초급:
- 모든 Level에서 그대로 사용한다.
- difficultWords에 넣지 않는다.

중급:
- Level 1에서는 쉬운 말로 바꾼다.
- Level 2에서는 원단어를 유지한다.
- Level 3에서는 원단어를 유지한다.
- Level 2에서만 필요한 경우 difficultWords에 넣는다.

고급:
- Level 1에서는 쉬운 말로 바꾼다.
- Level 2에서는 원단어를 유지하고 difficultWords에 넣는다.
- Level 3에서는 원단어를 유지하고 difficultWords에 넣는다.

school_core:
- 학교 문서 이해에 필요한 핵심어다.
- 단어를 삭제하지 않는다.
- Level 1에서는 쉬운 말로 바꾸거나 본문 안에서 바로 설명한다.
- Level 2에서는 원단어를 유지하고 짧게 설명한다.
- Level 3에서는 원단어를 유지한다.
- difficultWords에 넣는다.

없음:
- 바로 어려운 단어로 보지 않는다.
- 학교명, 기관명, 지명, 행사명, 프로그램명, 서비스명이면 고유명사로 보고 그대로 쓴다.
- 날짜, 시간, 전화번호, 금액, 인원, 기간이면 정보값으로 보고 그대로 쓴다.
- 여러 단어가 붙은 복합어이면 뜻을 나누어 판단한다.
- 학교 문서에서 반복되는 핵심어이면 school_core처럼 처리한다.
- 위에 해당하지 않는 어려운 일반어이면 고급처럼 처리한다.

# 8. 표현 유형별 변환 규칙

문구를 그대로 치환하지 않는다.
표현의 역할을 판단하고 문맥에 맞게 쉬운 문장으로 바꾼다.

1. 행정 명사형 표현
행정 명사형은 학생이 이해할 수 있는 행동 문장으로 바꾼다.
- 여부 → 할지 말지 / 원하는지 아닌지
- 제출 → 내요
- 신청 → 하겠다고 표시해요
- 응답 → 표시해요 / 답해요
- 조사 → 물어보는 것
- 확인 → 살펴봐요

2. 보호자 대상 표현
보호자가 해야 하는 행동은 학생 기준으로 바꾼다.
- 보호자와 함께 확인해요
- 보호자와 함께 적어요
- 보호자에게 이름을 써 달라고 해요

3. 조건 표현
조건이 있는 문장은 조건과 결과를 나누어 쓴다.
숫자, 비율, 날짜, 시간은 의미를 바꾸지 않는다.
형식: [조건]이면 [결과]가 돼요.

4. 비용 표현
비용 표현은 누가 돈을 내는지 드러나게 바꾼다.
부담, 지출, 납부, 자부담은 돈을 내는 행동으로 풀어 쓴다.

5. 금지·주의·불이익 표현
금지, 주의, 불이익 표현은 삭제하지 않는다.
- 불가 → 할 수 없어요
- 미제출 → 내지 않음
- 간주 → ~로 봐요
- 제한 → 정해진 만큼만 할 수 있어요

# 9. 줄바꿈 규칙

줄바꿈은 문장이 끝난 뒤에만 넣는다.
문장 중간에서 줄을 끊지 않는다.
서술어 없는 조각 문장을 만들지 않는다.

나쁜 예:
"정해진 날짜까지 적어서\\n선생님께 제출해야 해요."

좋은 예:
"정해진 날짜까지 적어요.\\n선생님께 내요."

같은 의미 단위 안에서는 \\n으로 줄을 나눈다.
의미 단위가 바뀌면 \\n\\n으로 문단을 나눈다.

# 8. OCR 오류 처리

OCR 때문에 붙은 말은 자연스럽게 고친다.

금지:
- 기념품!입생
- 간담회간담회
- 신청은 신청은
- 신청방법신청은 신청방법
- 모집인원모집인원

수정:
- 기념품과 간식을 받을 수 있어요.
- 안내된 방법으로 신청해요.
- 정해진 인원까지 신청할 수 있어요.

# 9. difficultWords 작성 규칙

difficultWords에는 쉬운글 본문에 실제로 남긴 어려운 단어만 넣는다.

- 초급 단어는 넣지 않는다.
- 같은 단어는 한 번만 넣는다.
- meaning은 초등학교 3~4학년이 이해할 수 있게 한 문장으로 쓴다.
- example은 학교생활 상황에 맞게 한 문장으로 직접 만든다.
- KRDICT 뜻과 예문을 그대로 복사하지 않는다.

displayMode:
- school_core, 고급, 어려운 없음 단어:
  { "level1": "inline", "level2": "tooltip", "level3": "tooltip" }
- 중급:
  { "level1": "none", "level2": "tooltip", "level3": "none" }

# 10. 출력 전 점검

출력하기 전에 확인한다.

1. 원문 순서가 유지되었는가?
2. 원문 정보가 이유 없이 삭제되지 않았는가?
3. 인사말, 감사 표현, 학교 구호 외의 내용을 과하게 생략하지 않았는가?
4. 원문에 없는 정보를 만들지 않았는가?
5. 같은 말이 붙어서 반복되지 않았는가?
6. level1, level2, level3의 단어 난이도 차이가 분명한가?
7. 줄바꿈이 들어가 있는가?

# 11. 출력 형식

반드시 JSON만 출력한다.
마크다운 코드블록은 쓰지 않는다.
문자열 안의 줄바꿈은 \\n으로 표시한다.
문단 구분은 \\n\\n으로 표시한다.

{
  "level1": "string",
  "level2": "string",
  "level3": "string",
  "difficultWords": [
    {
      "word": "string",
      "meaning": "string",
      "example": "string",
      "displayMode": {
        "level1": "inline" | "tooltip" | "none",
        "level2": "tooltip" | "none",
        "level3": "tooltip" | "none"
      }
    }
  ]
}

DOCUMENT_TYPE:
${documentType ?? ''}

ACTION_STEPS:
${JSON.stringify(actionSteps ?? [], null, 2)}

INPUT_TEXT:
${rawText}

CORE_FIELDS:
${JSON.stringify(coreFields, null, 2)}

WORD_CLASSIFICATIONS:
${wordSummary}
`;
}
